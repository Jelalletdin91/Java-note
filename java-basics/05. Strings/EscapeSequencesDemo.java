public class EscapeSequencesDemo {
    public static void main(String[] args) {

        // Insert a tab: Demonstrating a table of items and prices
        System.out.println("Item\t\tPrice");
        System.out.println("Apple\t\t$1.00");
        System.out.println("Banana\t\t$0.75");
        System.out.println("Orange\t\t$0.80");

        // Insert a new line: Writing a paragraph with multiple lines
        System.out.println("\nWelcome to our online store!\nWe offer fresh fruits and vegetables.\nThank you for shopping with us!");

        // Insert double quotes: Quoting a phrase or a slogan
        System.out.println("\nOur store's slogan is: \"Freshness at its best!\"");

        // Insert a backslash: Showing file paths in a system
        System.out.println("\nSave your order at: C:\\Users\\Customer\\Documents\\OrderSummary.txt");

    }
}