public class VariablesDemoThree {
    public static void main(String[] args) {

        // Character type
        char grade = 'D';  // Single character enclosed in single quotes

        // Boolean type
        boolean isEmpty;   // Declaration of boolean variable
        isEmpty = false;    // Assignment of boolean value (true/false)


        // Print the value of character type variable
        System.out.println("grade = " + grade);

        // Print the value of boolean type variable
        System.out.println("isEmpty = " + isEmpty);
    }
}