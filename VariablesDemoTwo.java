public class VariablesDemoTwo {
    public static void main(String[] args) {

        // Floating point data types
        float temperature = 25.6f;   // "f" or "F" is needed to specify a float value
        double percentage = 55.7;    // "d" or "D" is optional for double (default type for decimals)
        double average = 76.2d;      // "d" is explicitly added but not required


        // Print the values of floating point type variables
        System.out.println("temperature = " + temperature);
        System.out.println("percentage = " + percentage);
        System.out.println("average = " + average);
    }
}